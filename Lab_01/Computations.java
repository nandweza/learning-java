public class Computations {
    public static void main(String[] args) {
        double firstDouble = 13.56;
        double secondDouble = 8.65;

        double sumOfDoubles = firstDouble + secondDouble;

        double differenceOfDoubles = firstDouble - secondDouble;

        double productOfDoubles = firstDouble * secondDouble;

        System.out.println(sumOfDoubles);
        System.out.println(differenceOfDoubles);
        System.out.println(productOfDoubles);
    }
}
